const totalUniquePos = input[0].length - 1; // counted from 0

function treesEncountered(input, toboPos = 0, right = 3, down = 1) {
  let totalTreesFound = 0;
  for (let i = 0; i < input.length; i += down) {
    const curr = input[i][toboPos];
    if (curr === "#") {
      totalTreesFound++;
    }
    toboPos += right;
    toboPos > totalUniquePos ? (toboPos -= totalUniquePos + 1) : toboPos;
  }

  return totalTreesFound;
}

console.log(treesEncountered(input, 0, 3, 1));

