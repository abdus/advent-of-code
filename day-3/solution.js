const routePattern = [
  [1, 1],
  [3, 1],
  [5, 1],
  [7, 1],
  [1, 2],
];
const treesEncountered__arr = [];

for (let i in routePattern) {
  const rp = routePattern[i];
  const treesEnc = treesEncountered(input, 0, rp[0], rp[1]);
  treesEncountered__arr.push(treesEnc);
}

